  puts "Треугольник прямоугольный"  #объявляем, что треугольник прямоугольный
elsif   (a**2 + b**2 == c**2) && (a == b) #если сумма квадратов катетов равна квадрату гипотенузы и катеты равны между собой
  puts "Треугольник равнобедренный, прямоугольный" #объявляем, что треугольник равнобедренный, прямоугольный
elsif a == b && b == c && c == a    #Если стороны треугольника равны между собой
  puts "Треугольник равносторонний" #то треугольник равносторонний
elsif a == b                        #если между собой равны только две стороны
  puts "Треугольник равнобедренный" #то треугольник равнобедренный
else

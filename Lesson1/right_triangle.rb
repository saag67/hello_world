sides = [3]                        #создаем массив из трех символом для хранения данных

for i in 1..3                      #при помощи цикла вводим данные пользователя
  puts "Введите длину #{i}-й стороны треугольника"
  sides[i-1]=gets.chomp.to_f       #получаем данные от пользователя, обрезаем символ перевода строки, приводим к вещественному виду, сохраняем в массив
end
sides=sides.sort                   #сортируем массив, наибольший элемент получается последним, он будет гипотенузой

c = sides[2]                       #Присваиваем элементы массива переменным, это будет гипотенуза
b = sides[1]                       #Это будет один катет
a = sides[0]                       #Это второй катет


eq=(a**2 + b**2) <=> c**2    #присваем переменной eq результат сравнения суммы квадратов катетов и квадрата гипотенузы

if (eq == 0) && (a == b)  #если сумма квадратов катетов равна квадрату гипотенузыб и катеты равны
  puts "Треугольник прямоугольный, равнобедренный"  #объявляем, что треугольник прямоугольный, равнобедренный
elsif   (eq == 0)  #если сумма квадратов катетов равна квадрату гипотенузы
  puts "Треугольник прямоугольный" #объявляем, что треугольник  прямоугольный
elsif a == b && b == c && c == a    #Если стороны треугольника равны между собой
  puts "Треугольник равносторонний" #то треугольник равносторонний
elsif c == a || c == b          #если между собой равны только две стороны
  puts "Треугольник равнобедренный" #то треугольник равнобедренный
else
  puts "Это другая фигура"          #если получилось что-то непредусмотренное
end

  

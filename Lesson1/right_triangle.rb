
sides = []                        #создаем массив из трех символом для хранения данных
counter=0
3.times do 
  counter+=1                       #при помощи цикла вводим данные пользователя
  puts "Введите  длину #{counter}-й  стороны треугольника"
                                  #получаем данные от пользователя, обрезаем символ перевода строки, приводим к вещественному виду, сохраняем в массив
  sides << gets.to_f
end
sides.sort!                       #сортируем массив, наибольший элемент получается последним, он будет гипотенузой

c = sides[2]                       #Присваиваем элементы массива переменным, это будет гипотенуза
b = sides[1]                       #Это будет один катет
a = sides[0]                       #Это второй катет

